    public function getLocation(){
        $q = Input::get('term');

        $qtag = new MongoRegex('/'.$q.'/i');

        $res = Homeslider::where('widgetLocation',$qtag)->distinct('widgetLocation')->get()->toArray();

        $result = array();

        foreach($res as $r){
            $result[] = array('id'=>$r[0],'label'=>$r[0],'value'=>$r[0]);
        }

        return Response::json($result);

    }

